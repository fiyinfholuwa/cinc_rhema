        }
        
        function clearError(fieldId) {
            const field = document.getElementById(fieldId);
            const errorDiv = document.getElementById(fieldId + 'Error');
            field.classList.remove('error');
            errorDiv.classList.remove('show');
        }
        
        function validateField(fieldId, value, validationType) {
            if (!value || value.trim() === '') {
                showError(fieldId, 'This field is required');
                return false;
            }
            
            if (validationType === 'email' && !validateEmail(value)) {
                showError(fieldId, 'Please enter a valid email address');
                return false;
            }
            
            if (validationType === 'phone' && !validatePhone(value)) {
                showError(fieldId, 'Please enter a valid phone number (at least 10 digits)');
                return false;
            }
            
            if (validationType === 'name' && value.trim().length < 2) {
                showError(fieldId, 'Name must be at least 2 characters');
                return false;
            }
            
            showSuccess(fieldId);
            return true;
        }
        
