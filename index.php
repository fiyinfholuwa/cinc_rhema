        function showAlert(message, type) {
    // type can be 'success', 'error', 'warning', or 'info'
    switch (type) {
        case 'success':
            toastr.success(message);
            break;
        case 'error':
            toastr.error(message);
            break;
        case 'warning':
            toastr.warning(message);
            break;
        case 'info':
        default:
            toastr.info(message);
            break;
    }
}

